function create() {
  document.getElementById("ex-options").style.display = "none";
  document.getElementById("pre-options").style.display = "";
  document.getElementById("title").innerText = "Tùy chỉnh bài tập";
  if (excercise.auto == true) {
    if (excercise.goal == 1) {
      excercise.ex_num = [excercise.group.length, excercise.group.length];
      if (excercise.ex_num <= 2) {
        excercise.ex_num = [excercise.group.length * 2, excercise.group.length * 2];
      }
      for (let i = 2; i <= excercise.ex_round; i++) {
        document.getElementById(`round-${i}`).style.display = "";
      }
      let name = [];
      for (let i = 1; i <= excercise.ex_num[0] + excercise.ex_num[1]; i++) {
        for (let x = 0; x < excercise.group.length; x++) {
          for (let y = 0; y < ex_lib.length; y++) {
            if (
              ex_lib[y].group == excercise.group[x] &&
              ex_lib[y].level == excercise.level &&
              name.includes(ex_lib[y].name) == false
              // excercise.ex_list.includes(ex_lib[y]) == false
            ) {
              excercise.ex_list.push(ex_lib[y]);
              name.push(ex_lib[y].name)
            }
          }
        }
      }
      for (let i = 1; i <= excercise.ex_num[0] + excercise.ex_num[1]; i++) {
        y = i;
        if (y > 3) {
          y += 4;
        }
        document.getElementById(`ex-${y}`).style.display = "";
        document.getElementById(`name-${y}`).innerText =
          excercise.ex_list[i - 1].name;
        document.getElementById(`rep-${y}`).innerText =
          excercise.ex_list[i - 1].rep;
        document.getElementById(`rest-${y}`).innerText =
          excercise.ex_list[i - 1].rest;
        document.getElementById(`img-${y}`).src =
          excercise.ex_list[i - 1].image;
      }
    } else if (excercise.goal == 2) {
      excercise.ex_num = [5];
      for (let i = 1; i <= excercise.ex_num; i++) {
        for (let y = 0; y < ex_lib.length; y++) {
          if (
            ex_lib[y].goal == 2 &&
            excercise.ex_list.includes(ex_lib[y]) == false
          ) {
            excercise.ex_list.push(ex_lib[y]);
          }
        }
      }
      for (let i = 1; i <= excercise.ex_num; i++) {
        document.getElementById(`ex-${i}`).style.display = "";
        document.getElementById(`name-${i}`).innerText =
          excercise.ex_list[i - 1].name;
        document.getElementById(`rep-${i}`).innerText =
          excercise.ex_list[i - 1].rep;
        document.getElementById(`rest-${i}`).innerText =
          excercise.ex_list[i - 1].rest;
        document.getElementById(`img-${i}`).src =
          excercise.ex_list[i - 1].image;
      }
    } else {
      excercise.ex_num = [0];
    }
  } else {
    excercise.ex_round = 1;
    excercise.ex_num = [1];
    excercise.ex_list.push(ex_lib[0]);
    document.getElementById("ex-1").style.display = "";
    document.getElementById("name-1").innerText = excercise.ex_list[0].name;
    document.getElementById("rep-1").innerText = excercise.ex_list[0].rep;
    document.getElementById("rest-1").innerText = excercise.ex_list[0].rest;
    document.getElementById("img-1").src = excercise.ex_list[0].image;
  }
}

